// the concat method is used to merge two arrays
// remember it always return a new array and does not mutate the original arrays

//example
const array1 = [2, 4, 5, 4, 56, 46, 5];
const array2 = [4, 65, 45, 6, 4, 54, 54];

const array3 = array1.concat(array2);

//this will return a new array of the two arrays merged together
